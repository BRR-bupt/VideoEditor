<template>
  <sp-button size="S" type="secondary" @click="clickInput">
    <slot />
    <input ref="input" type="file" style="display: none" @change="change" />
  </sp-button>
</template>

<script lang="ts">
import Vue from 'vue'
import { Component, Prop, Ref } from 'vue-property-decorator'
import { ProjError } from '../../plugins/error'

@Component({})
export default class FileButton extends Vue {
  @Ref() input!: HTMLInputElement

  @Prop({ default: false }) multiple!: boolean

  clickInput() {
    this.input.click()
  }

  change(e: MouseEvent) {
    const target = e.target as HTMLInputElement
    if (!target.files) return
    if (this.multiple) {
      this.$emit('change', target.files)
    } else {
      // 总是执行else
      if (target.files.length > 1) throw new ProjError('Not support multiple files.')
      const file = target.files[0]

      console.log(file)

      this.$emit('change', file)
    }
  }
}
</script>
